
export function setToken(token) {
    return {
        type: 'SET_TOKEN',
        payload: token
    }
}
export function setName(name) {
    return {
        type: 'SET_NAME',
        payload: name
    }
}
export function setDOB(dob) {
    return {
        type: 'SET_DOB',
        payload: dob
    }
}
export function setEmail(email) {
    return {
        type: 'SET_EMAIL',
        payload: email
    }
}
